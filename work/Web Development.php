                <h2>Hawk Yak</h2>
                <p>
                    When I came into Lehigh as a freshmen, Yik Yak was a very popular app used by many college students. However, the app was exclusive to phones and there was no website that mirrored local yaks onto the web. With the help of my roommate, we used an API to expand yakking capabilities to the browser. I designed the website which dynamically displayed local yaks. With the help of web.py, my partner transferred the local yak feed data from python into javasript, which I then handled.
                </p>
                <p>We also enabled voting on yaks (without having an account) so to prevent exessive vote manipulation we kept the website exclusive to only students connected to the Lehigh network</p>
                <p>Now, Yik Yak has finally released their own web app. Looking back, our vision of Yik Yak's web app is similar to what has been released. Hawk Yak allowed users to click yaks to expand to the comments. We also enabled effortless switching between new and hot yaks. As a startup, Yik Yak has been a fast growing company which introduced many new featured after we developed Hawk Yak. The API we used no longer works, so we discontinued the project even though we had many creative ideas planned for making Yik Yak more engaging and vibrant.</p>

