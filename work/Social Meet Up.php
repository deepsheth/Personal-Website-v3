                        A few months ago, my friends and I decided to participate in an app development competition (LU Apps). We were given just over a month to develop an app and present it at the final showcase, in front of judges, friends, and Lehigh students.
                    </p>
                    <p>
                        Before we started coding, we dedicated a week to think of an app idea. After juggling with a number of ideas, we agreed that we wanted an app that can help people feel connected in <em>real life</em>. Popular social media sites like Facebook make us feel <em>virtually connected</em> with friends. Specifically, we noticed a problem here. If you want to meet up with friends in person at campus events, it's difficult and roundabout. Some clubs only send out email reminders for events, while other clubs use <em>every</em> type of social media platform to inform you. Look inside any campus building: many outdated flyers still hang around for events that have passed. Organizations also chalk the sidewalks to publicize events. On top of this, some friends only text; others prefer using Facebook Messenger or GroupMe for communicating. How are you supposed to know which friends plan on attending an upcoming event? Should you really have to create a dozens of different group conversations to meet up with friends in person?
                    </p>
                </div>


                <div class="row">
                    <p class="col-md-6">
                        Ultimately, there isn't a cohesive way to find out what's going on around campus and what different friends plan on attending. This sparked our interest. We started developing a social event planning app. One where you can add friends and visually see exactly which off campus house or academic building an event is held in. You can see popular events many of your friends are attending, filter out events you dislike, and of course, invite friends to your private hangouts.
                    </p>
